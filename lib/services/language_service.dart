  }

  String getLanguageName(String code) {
    switch (code) {
      case 'hi': return 'Hindi';
      case 'mr': return 'Marathi';
      case 'ta': return 'Tamil';
      case 'gu': return 'Gujarati';
      case 'bn': return 'Bangla';
      case 'te': return 'Telugu';
      default: return 'English';
    }
  }

